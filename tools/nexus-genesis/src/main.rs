    #[test]
    fn test_generate_from_key_bundles() {
        use nexus_crypto::{
            DilithiumSigner, FalconSigner, KeyEncapsulationMechanism, KyberKem, Signer,
        };

        let dir = TempDir::new().unwrap();

        // Create 4 validator key bundles (simulating nexus-keygen output).
        let mut key_paths = Vec::new();
        for i in 0..4 {
            let (_, fvk) = FalconSigner::generate_keypair();
            let (_, dvk) = DilithiumSigner::generate_keypair();
            let (kek, _) = KyberKem::generate_keypair();

            let bundle = serde_json::json!({
                "falcon_verify_key": hex::encode(fvk.as_bytes()),
                "dilithium_verify_key": hex::encode(dvk.as_bytes()),
                "kyber_encaps_key": hex::encode(kek.as_bytes()),
            });

            let path = dir.path().join(format!("validator-{i}-public-keys.json"));
            fs::write(&path, serde_json::to_string_pretty(&bundle).unwrap()).unwrap();
            key_paths.push(path);
        }

        let output = dir.path().join("genesis.json");
        cmd_generate(&GenerateParams {
            chain_id: "test-chain".to_owned(),
            num_shards: 2,
            stake: Amount::ONE_NXS.0,
            validator_keys: key_paths.clone(),
            validator_names: vec![],
            network_peer_ids: random_peer_ids(key_paths.len()),
            timestamp: 1000,
            output: output.clone(),
            force: false,
        })
        .unwrap();

        let json = fs::read_to_string(&output).unwrap();
        let config: GenesisConfig = serde_json::from_str(&json).unwrap();
        config.validate().unwrap();
        assert_eq!(config.chain_id, "test-chain");
        assert_eq!(config.validators.len(), 4);
        assert_eq!(config.num_shards, 2);
        assert_eq!(config.genesis_timestamp.0, 1000);
        // With 2 shards, validators should be assigned round-robin.
        assert_eq!(config.validators[0].shard_id, Some(ShardId(0)));
        assert_eq!(config.validators[1].shard_id, Some(ShardId(1)));
        assert_eq!(config.validators[2].shard_id, Some(ShardId(0)));
    }

