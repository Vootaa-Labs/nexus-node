//! `nexus-genesis` — CLI tool for generating the Nexus genesis configuration.
//!
//! Produces a signed genesis configuration JSON file given validator public
//! key bundles and chain parameters. The resulting file is loaded by
//! `nexus-node` at first boot to initialise the chain state.
//!
//! # Usage
//!
//! ```text
//! # Generate from validator key bundles:
//! nexus-genesis generate \
//!     --chain-id nexus-testnet-1 \
//!     --num-shards 4 \
//!     --stake 1000000000 \
//!     --validator-keys ./validator-0/validator-public-keys.json \
//!     --validator-keys ./validator-1/validator-public-keys.json \
//!     --validator-keys ./validator-2/validator-public-keys.json \
//!     --validator-keys ./validator-3/validator-public-keys.json \
//!     --output genesis.json
//!
//! # Validate an existing genesis file:
//! nexus-genesis validate --input genesis.json
//!
//! # Generate a minimal test genesis:
//! nexus-genesis test-genesis --output genesis.json
//! ```

#![forbid(unsafe_code)]

use std::fs;
use std::path::{Path, PathBuf};
use std::time::{SystemTime, UNIX_EPOCH};

use anyhow::{Context, Result};
use clap::{Parser, Subcommand};
use nexus_config::{ConsensusConfig, GenesisConfig, GenesisValidatorEntry};
use nexus_primitives::{Amount, ShardId, TimestampMs};
use serde::Deserialize;

// ── CLI definitions ──────────────────────────────────────────────────────

/// Nexus genesis configuration generator.
///
/// Creates the initial chain configuration file that bootstraps the validator
/// set, token allocations, and consensus parameters.
#[derive(Parser)]
#[command(name = "nexus-genesis", version, about)]
struct Cli {
    #[command(subcommand)]
    command: Command,
}

#[derive(Subcommand)]
enum Command {
    /// Generate a genesis configuration from validator key bundles.
    Generate {
        /// Unique chain identifier (e.g., "nexus-testnet-1").
        #[arg(long)]
        chain_id: String,

        /// Number of execution shards.
        #[arg(long, default_value = "4")]
        num_shards: u16,

        /// Default stake per validator (NXS base units).
        #[arg(long, default_value_t = Amount::ONE_NXS.0)]
        stake: u64,

        /// Paths to validator-public-keys.json files (one per validator).
        #[arg(long = "validator-keys", required = true)]
        validator_keys: Vec<PathBuf>,

        /// Optional custom validator names (in same order as --validator-keys).
        #[arg(long = "validator-name")]
        validator_names: Vec<String>,

        /// Libp2p PeerIds (base58), one per validator, same order as --validator-keys.
        /// Generated by `nexus-keygen identity`.
        #[arg(long = "network-peer-id", required = true)]
        network_peer_ids: Vec<String>,

        /// Genesis timestamp in milliseconds since epoch (0 = now).
        #[arg(long, default_value = "0")]
        timestamp: u64,

        /// Output genesis JSON file path.
        #[arg(long, short, default_value = "genesis.json")]
        output: PathBuf,

        /// Overwrite existing output file.
        #[arg(long)]
        force: bool,
    },

    /// Validate an existing genesis configuration file.
    Validate {
        /// Path to the genesis JSON file to validate.
        #[arg(long, short)]
        input: PathBuf,
    },

    /// Generate a minimal test genesis (4 validators, random keys).
    TestGenesis {
        /// Output genesis JSON file path.
        #[arg(long, short, default_value = "genesis.json")]
        output: PathBuf,

        /// Overwrite existing output file.
        #[arg(long)]
        force: bool,
    },
}

/// Validator public key bundle (as output by `nexus-keygen validator`).
#[derive(Deserialize)]
struct ValidatorPublicKeys {
    falcon_verify_key: String,
    dilithium_verify_key: String,
    kyber_encaps_key: String,
}

// ── Main entry point ─────────────────────────────────────────────────────

fn main() -> Result<()> {
    let cli = Cli::parse();

    match cli.command {
        Command::Generate {
            chain_id,
            num_shards,
            stake,
            validator_keys,
            validator_names,
            network_peer_ids,
            timestamp,
            output,
            force,
        } => {
            let params = GenerateParams {
                chain_id,
                num_shards,
                stake,
                validator_keys,
                validator_names,
                network_peer_ids,
                timestamp,
                output,
                force,
            };
            cmd_generate(&params)
        }
        Command::Validate { input } => cmd_validate(&input),
        Command::TestGenesis { output, force } => cmd_test_genesis(&output, force),
    }
}

// ── Command implementations ──────────────────────────────────────────────

struct GenerateParams {
    chain_id: String,
    num_shards: u16,
    stake: u64,
    validator_keys: Vec<PathBuf>,
    validator_names: Vec<String>,
    network_peer_ids: Vec<String>,
    timestamp: u64,
    output: PathBuf,
    force: bool,
}

fn cmd_generate(params: &GenerateParams) -> Result<()> {
    check_overwrite(&params.output, params.force)?;

    if params.network_peer_ids.len() != params.validator_keys.len() {
        anyhow::bail!(
            "--network-peer-id count ({}) must match --validator-keys count ({})",
            params.network_peer_ids.len(),
            params.validator_keys.len()
        );
    }

    let genesis_timestamp = if params.timestamp == 0 {
        let now = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .context("system clock before Unix epoch")?;
        TimestampMs(now.as_millis() as u64)
    } else {
        TimestampMs(params.timestamp)
    };

    let mut validators = Vec::with_capacity(params.validator_keys.len());

    for (i, key_path) in params.validator_keys.iter().enumerate() {
        let json = fs::read_to_string(key_path)
            .with_context(|| format!("failed to read validator keys: {}", key_path.display()))?;
        let keys: ValidatorPublicKeys = serde_json::from_str(&json)
            .with_context(|| format!("failed to parse validator keys: {}", key_path.display()))?;

        let name = params
            .validator_names
            .get(i)
            .cloned()
            .unwrap_or_else(|| format!("validator-{i}"));

        validators.push(GenesisValidatorEntry {
            name,
            network_peer_id: params.network_peer_ids[i].clone(),
            falcon_verify_key_hex: keys.falcon_verify_key,
            dilithium_verify_key_hex: keys.dilithium_verify_key,
            kyber_encaps_key_hex: keys.kyber_encaps_key,
            stake: Amount(params.stake),
            shard_id: if params.num_shards > 1 {
                Some(ShardId((i as u16) % params.num_shards))
            } else {
                None
            },
        });
    }

    let config = GenesisConfig {
        chain_id: params.chain_id.clone(),
        genesis_timestamp,
        num_shards: params.num_shards,
        validators,
        allocations: Vec::new(),
        consensus: ConsensusConfig::default(),
    };

    config
        .validate()
        .context("generated genesis config failed validation")?;

    write_genesis_json(&config, &params.output)?;

    let n = config.validators.len();
    let f = (n.saturating_sub(1)) / 3;
    eprintln!(
        "Genesis configuration written to {}",
        params.output.display()
    );
    eprintln!("  chain_id:    {}", config.chain_id);
    eprintln!("  validators:  {n} (f={f}, quorum={})", 2 * f + 1);
    eprintln!("  shards:      {}", config.num_shards);
    eprintln!("  timestamp:   {}", config.genesis_timestamp.0);

    Ok(())
}

fn cmd_validate(input: &Path) -> Result<()> {
    let json = fs::read_to_string(input)
        .with_context(|| format!("failed to read genesis file: {}", input.display()))?;
    let config: GenesisConfig = serde_json::from_str(&json)
        .with_context(|| format!("failed to parse genesis file: {}", input.display()))?;

    config.validate().context("genesis validation failed")?;

    let n = config.validators.len();
    let f = (n.saturating_sub(1)) / 3;
    eprintln!("Genesis configuration is VALID");
    eprintln!("  chain_id:      {}", config.chain_id);
    eprintln!("  validators:    {n} (f={f}, quorum={})", 2 * f + 1);
    eprintln!("  shards:        {}", config.num_shards);
    eprintln!("  allocations:   {}", config.allocations.len());
    eprintln!("  total supply:  {}", config.total_supply());

    Ok(())
}

fn cmd_test_genesis(output: &Path, force: bool) -> Result<()> {
    check_overwrite(output, force)?;

    let config = GenesisConfig::for_testing();
    config
        .validate()
        .context("test genesis validation failed")?;

    write_genesis_json(&config, output)?;

    eprintln!("Test genesis written to {}", output.display());
    eprintln!("  chain_id:    {}", config.chain_id);
    eprintln!("  validators:  {}", config.validators.len());

    Ok(())
}

// ── Helpers ──────────────────────────────────────────────────────────────

fn write_genesis_json(config: &GenesisConfig, path: &Path) -> Result<()> {
    if let Some(parent) = path.parent() {
        if !parent.as_os_str().is_empty() && !parent.exists() {
            fs::create_dir_all(parent)
                .with_context(|| format!("failed to create directory: {}", parent.display()))?;
        }
    }
    let json =
        serde_json::to_string_pretty(config).context("failed to serialize genesis config")?;
    fs::write(path, &json).with_context(|| format!("failed to write: {}", path.display()))?;
    Ok(())
}

fn check_overwrite(path: &Path, force: bool) -> Result<()> {
    if path.exists() && !force {
        anyhow::bail!(
            "file already exists: {} (use --force to overwrite)",
            path.display()
        );
    }
    Ok(())
}

